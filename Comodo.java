package Atividades_Gabriela_Miranda;

public class Comodo {
	//atributos
	private String nome;

	
	//construtor
	public Comodo() {
		super();
	}
	
	public Comodo( String n) {
		this.nome = n;
	}
	
	//get e set
	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
}
